# =============================================================
# BUILDER FUNCTION — assembles expertise for system prompt
# =============================================================

def build_marketing_expertise(platform: str, content_type: str) -> str:
    """
    Build the marketing expertise section for the system prompt.
    Combines: core expertise + platform-specific + content-type-specific + emotional triggers.
    """
    sections = [MARKETING_EXPERTISE]

    # Platform-specific expertise
    platform_lower = platform.lower()
    if platform_lower in PLATFORM_EXPERTISE:
        sections.append(PLATFORM_EXPERTISE[platform_lower])

    # Content-type-specific expertise
    if content_type in CONTENT_TYPE_EXPERTISE:
        sections.append(CONTENT_TYPE_EXPERTISE[content_type])

    # Always add emotional triggers for Israeli audience
    sections.append(EMOTIONAL_TRIGGERS)

    return "\n".join(sections)
